package ZigZagAlgorithmGeneric;



public class item{
      TrackType type;
      int value;
      
	public item(TrackType type, int value) {
		super();
		this.type = type;
		this.value = value;
	}

	public TrackType getType() {
		return type;
	}

	public void setType(TrackType type) {
		this.type = type;
	}

	

	public void setValue(int value) {
		this.value = value;
	}

	public int getValue() {
		return value;
	}
      
     

}
